    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtrech.getText().equals("")) {
            
            JOptionPane.showMessageDialog(this, "Field cannot be empty!");
        } else { 
            if (comrech.getSelectedItem().equals("id_res")) {
                ReservationServiceImplement res = new ReservationServiceImplement();
                rs= res.getAllReservationWhere("id_res LIKE '%" + txtrech.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            } else if (comrech.getSelectedItem().equals("date_res")) {
                ReservationServiceImplement res = new ReservationServiceImplement();
                rs= res.getAllReservationWhere("dateres LIKE '%" + txtrech.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));
            }
            else if (comrech.getSelectedItem().equals("arrival_date")) {                
                ReservationServiceImplement res = new ReservationServiceImplement();
                rs= res.getAllReservationWhere("datearr LIKE '%" + txtrech.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));           
            }
            else if (comrech.getSelectedItem().equals("departure_date")) {
                ReservationServiceImplement res = new ReservationServiceImplement();
                rs= res.getAllReservationWhere("datedep LIKE '%" + txtrech.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));          
            } 
            else if (comrech.getSelectedItem().equals("person_number")) {
                ReservationServiceImplement res = new ReservationServiceImplement();
                rs= res.getAllReservationWhere("nbrepersonne LIKE '%" + txtrech.getText() + "%' ");
                jTable1.setModel(new ResultSetTableModel(rs));           
            } 
            
        }
      
    }//GEN-LAST:event_jButton1ActionPerformed

