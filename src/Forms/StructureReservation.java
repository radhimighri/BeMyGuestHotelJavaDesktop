    private void ModifybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifybtnActionPerformed

        if (arrDate.getText().equals("") || resDate.getText().equals("") || DepDate.getText().equals("")
            || PersonNbr.getText().equals("") ) 
        {
            JOptionPane.showMessageDialog(this, "Please fill all the fields ! ");
                 DepDate.setBackground(red);
                 arrDate.setBackground(red);
                 resDate.setBackground(red);
                 PersonNbr.setBackground(red);  
                 ClientSelect.setBackground(red);
        }
          else if( isNumeric(PersonNbr.getText()) == false )
              {            JOptionPane.showMessageDialog(this, "Person number must be a numeric !!! ");
                           PersonNbr.setBackground(red);

              } 
            else if (ClientSelect.getSelectedItem()== "Choose Client"){
        JOptionPane.showMessageDialog(this, "Please select a Client!");
         ClientSelect.setBackground(red);

        }
        
        else {
            Date datearr = null;
            Date dateres = null;
            Date datedep = null;

    SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy/MM/dd");

    String date1 = arrDate.getText();
    String date2 = resDate.getText();
    String date3 = DepDate.getText();

    try {
      datearr = simpleDateFormat.parse(date1);
      dateres = simpleDateFormat.parse(date2);
      datedep = simpleDateFormat.parse(date3);      
     if(datedep.compareTo(datearr)<0){
     JOptionPane.showMessageDialog(this, "Departure date must be later than the arrival one !");
                 DepDate.setBackground(red);
                 arrDate.setBackground(red);


}

     else if((datedep.compareTo(dateres)<0)){
         JOptionPane.showMessageDialog(this, "Departure date must be later than the reservation one !");
         DepDate.setBackground(red);
         resDate.setBackground(red);

     }
     
     else if((dateres.compareTo(datearr)>0)){
         JOptionPane.showMessageDialog(this, "Arrival date must be later than the reservation one !");
         resDate.setBackground(red);
         arrDate.setBackground(red);

     }

            else {         
            String id = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            Reservation r = new Reservation(null,date2,date1,date3,PersonNbr.getText(),ClientSelect.getSelectedItem().toString());
            ReservationServiceImplement fact = new ReservationServiceImplement();
            fact.EditReservation(r,id);
                table();
                 DepDate.setBackground(green);
                 arrDate.setBackground(green);
                 resDate.setBackground(green);
                 PersonNbr.setBackground(green);
           JOptionPane.showMessageDialog(this, "Reservation was updated with success");       } 
    } catch (ParseException e) {
              System.out.println("Errerrrrr");
     JOptionPane.showMessageDialog(this, "Please verify the dates format (DD/MM/YYYY) !");
    }

        }

    }//GEN-LAST:event_ModifybtnActionPerformed

